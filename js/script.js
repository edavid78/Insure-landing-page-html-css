function myopenFunction() {
    document.getElementById("closeMobilenav").style.display = "block";
    document.getElementById("openMobilenav").style.display = "none";
    document.getElementById("overlay").style.display = "block";
}

function mycloseFunction() {
    document.getElementById("closeMobilenav").style.display = "none";
    document.getElementById("openMobilenav").style.display = "block";
    document.getElementById("overlay").style.display = "none";
}
